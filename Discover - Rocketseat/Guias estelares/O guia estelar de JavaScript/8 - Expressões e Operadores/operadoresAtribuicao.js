// Operadores de atribuição (Assignment)

let x;

console.log(x);

// assignment
x = 1;
console.log(x);

// addition assignment
x += 2;
console.log(x);

// subtraction assignment
x -= 1;
console.log(x);

// multiplication assignment
x *= 2;
console.log(x);

// division assingment
x /= 2;
console.log(x);

// remainder assignment
x %= 2;
console.log(x);

// exponentiation assignment
x **= 2;
console.log(x);