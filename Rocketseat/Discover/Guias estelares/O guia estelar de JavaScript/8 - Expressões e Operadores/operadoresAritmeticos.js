// Operadores Aritméticos

// Multiplicação (*)
console.log(3 * 5);

// Divisão (/)
console.log(12 / 2);

// Soma (+)
console.log(34 + 67);

// Subtração (-)
console.log(34 - 23);

// Resto da divisão (%)
let remainder;
remainder = 11 % 9;
console.log(remainder)

// Incremento (++)
let increment = 0;
increment++;
console.log(increment);

// Decremento (--)
let decrement = 0;
decrement--;
console.log(decrement);

// Exponencial (**)
console.log(3 ** 3);