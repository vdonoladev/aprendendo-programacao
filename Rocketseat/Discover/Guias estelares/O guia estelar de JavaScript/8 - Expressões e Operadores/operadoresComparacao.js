// Operadores de comparação

// Irá comparar valores e retornar um Boolean como resposta à comparação

let one = 1;
let two = 2;

// ==     Igual
console.log(one == two);

// !=     Diferente
console.log(one != two);

// ===    Estritamente igual
console.log(one === two);

// !==    Estritamente diferente
console.log(one !== two);

// >      Maior
console.log(one > two);

// >=     Maior ou igual
console.log(one >= two);

// <      Menor
console.log(one < two);

// <=     Menor ou igual
console.log(one <= two);
