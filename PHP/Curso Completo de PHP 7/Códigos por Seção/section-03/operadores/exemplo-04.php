<?php
// Operadores de comparação.

$a = 30;
$b = 55;

// > Maior que.
var_dump($a > $b);

echo "<br><br><br><br>";

// < Menor que.
var_dump($a < $b);

echo "<br><br><br><br>";

// = Atribuição.
var_dump($a = $b);

echo "<br><br><br><br>";

// == Comparação de valor.
var_dump($a == $b);

echo "<br><br><br><br>";

// === Comparação de valor e tipo.
var_dump($a === $b);

echo "<br><br><br><br>";

// != Valor diferente.
var_dump($a != $b);

echo "<br><br><br><br>";

// !== Valor e tipo diferente.
var_dump($a !== $b);

?>