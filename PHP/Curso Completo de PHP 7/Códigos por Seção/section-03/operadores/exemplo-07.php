<?php
// Operador de incremento e decremento

$a = 10;

// Pós-incremento.
echo "Pós-incremento: ";
echo $a++;
echo " => ";
echo $a;

echo "<br><br><br><br>";

echo "Pré-incremento: ";
$b = 20;
echo ++$b;

echo "<br><br><br><br>";

$c = 15;

echo "Pós-decremento: ";
echo $c--;
echo " => ";
echo $c;

echo "<br><br><br><br>";

echo "Pré-decremento: ";
$d = 20;
echo --$d;

?>